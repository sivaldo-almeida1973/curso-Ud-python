"""
Fatiamento de strings

[i:f:p] [::]

len -> retorna qtde de caractere
"""

variavel = 'Ol√°, mundo!'
print(variavel[-4]) #imprimir o indice -4
print(variavel[4]) #imprimir o indice 4
print(variavel[4:]) # do 4 ate o final
print(variavel[:4]) # do inicio ate o 3
print(variavel[4:8])
print(variavel[0:11:1]) #imprimir o indice -4
print(variavel[0:11:2]) #imprimir do 0 ao 11 (de 2 em 2)
print(variavel[::-1]) #imprimir ao contrario


print(len(variavel)) # quantas caracteres tem na strings






