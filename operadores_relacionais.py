"""
Opreadores Relacionais

>= maior ou igual
<= menor ou ugual
== igual
!= diferente

""" 

maior = 2 > 1
maior_igual = 2 >= 1

menor = 1 < 2
menor_igual = 2 <= 2

igual = 'a' == 'a'

diferente = 'a' != 'b'

print(maior)
print(maior_igual)
print(menor)
print(menor_igual)
print(igual)
print(diferente)


